public class tasksheet113 {
    public static void main(String[] args) {
        // Declare primitive variables
        byte zero = 0;
        short three = 3;
        int one = 1;
        float twoPointZero = 2.0f;
        boolean isTrue = true;

        // Concatenate primitives into a string to form "H3110 w0r1d 2.0 true"
        String output = "H" + three + one + one + zero + " w" + zero + "r" + one + "d " + twoPointZero + " " + isTrue;

        // Print the result
        System.out.println(output);
    }
}
